// Helper functions
export const getTrainingCategoryById = (id: string): TrainingCategory | undefined => {
  return trainingCategories.find(c => c.id === id);
};

export const getTrainingArticle = (categoryId: string, slug: string): TrainingArticle | undefined => {
  const category = getTrainingCategoryById(categoryId);
  return category?.articles.find(a => a.slug === slug);
};
